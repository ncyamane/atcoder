#include <bits/stdc++.h>
using namespace std;
int main()
{
    int64_t a = 2000000000;
    int64_t b = a * 2;
    cout << a << endl;
    cout << b << endl;

    int64_t c = (a * 10 + 100) / 100;
    cout << c << endl;
    cout << 2000000000 * 2 << endl;
    cout << 2000000000LL * 2 << endl;
    cout << 2000000000 * 2LL << endl;
    cout << 2000000000LL * 2LL << endl;
}
